#include<stdio.h>
void main()
{
    printf("\a:alarm or bell");
    printf("backspace \b:backspace.\n");
    printf("\f:form feed.\n");
    printf("\n:new line.\n");
    printf("carriage return \r:carriage return.\n");
    printf("\t:tab(horizontal).\n");
    printf("\v:vertical tab.\n");
    printf("\\:backlash'\n");
    printf("\':single quote.\n");
    printf("\":double quote.\n");
    printf("\?:question mark.\n");
    printf("\006:octal number.\n");
    printf("\x0ab:hexadecimal number.\n");
    printf("\0:NULL.\n");
    printf("%%:percentage symbol");
    printf("Format specifier is %%d for an int data type.\n");
    printf("Format specifier is %%f for an float data type.\n");
    printf("Format specifier is %%c for an char data type.\n");
    printf("Thank you.\n");
}
